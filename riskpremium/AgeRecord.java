package nl.yasmijn.riskpremium;


public class AgeRecord {
	private int age;
	private double lx;
	private double qx;
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public double getLx() {
		return lx;
	}
	public void setLx(double lx) {
		this.lx = lx;
	}
	public double getQx() {
		return qx;
	}
	public void setQx(double qx) {
		this.qx = qx;
	}
}
